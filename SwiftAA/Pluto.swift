import Foundation

/// The Pluto dwarf planet.
public class Pluto: Planet {

    /// The average color of the planet.
    public class override var averageColor: Color {
        get { return Color(red: 0.776, green:0.620, blue:0.486, alpha: 1.0) }
    }
}

